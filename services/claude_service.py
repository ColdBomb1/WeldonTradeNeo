def _extract_text(response) -> str:
    """Extract text from a Claude response, handling thinking blocks.

    With adaptive thinking enabled, response.content may contain both
    'thinking' and 'text' blocks. This returns the first text block.
    """
    for block in response.content:
        if hasattr(block, "text") and block.type == "text":
            return block.text
    # Fallback: find any block with a .text attribute
    for block in response.content:
        if hasattr(block, "text"):
            return block.text
    # Last resort: concatenate thinking blocks (better than crashing)
    for block in response.content:
        if hasattr(block, "thinking") and block.thinking:
            logger.warning("No text block in response — falling back to thinking block")
            return block.thinking
    return ""


def _review_create(client, cfg, system: str, prompt: str, **kwargs):
    """Call Claude for review/evolve/training analysis.

    Previously routed to Opus with adaptive thinking at max effort. That was
    removed as a safety measure — the app is locked to Sonnet-only. This
    function now uses the standard Sonnet model and parameters. The output
    will be less thorough than the old Opus path, but it cannot burn Opus
    tokens.
    """
    return client.messages.create(
        model=cfg.claude.model,
        max_tokens=min(cfg.claude.max_tokens, 4096),
        system=system,
        messages=[{"role": "user", "content": prompt}],
        **kwargs,
    )


def _parse_json_response(text: str) -> dict | list | None:
    """Extract JSON object or array from Claude's response.

    Handles code blocks, raw JSON, and extra trailing content.
    """
    # Try code blocks first
    code_block = re.search(r"```(?:json)?\s*\n?(.*?)```", text, re.DOTALL)
    if code_block:
        json_str = code_block.group(1).strip()
        result = _try_parse_json(json_str)
        if result is not None:
            return result

    # Try the full text as-is
    result = _try_parse_json(text.strip())
    if result is not None:
        return result

    # Find first [ or { and try to parse from there
    for start_char, end_char in [("[", "]"), ("{", "}")]:
        start_idx = text.find(start_char)
        if start_idx == -1:
            continue
        # Find matching closing bracket by counting nesting
        depth = 0
        for i in range(start_idx, len(text)):
            if text[i] == start_char:
                depth += 1
            elif text[i] == end_char:
                depth -= 1
                if depth == 0:
                    json_str = text[start_idx:i + 1]
                    result = _try_parse_json(json_str)
                    if result is not None:
                        return result
                    break

    logger.warning("Could not extract JSON from response (len=%d)", len(text))
    return None


def _try_parse_json(text: str) -> dict | list | None:
    """Try to parse JSON, return None on failure (no logging)."""
    try:
        return json.loads(text)
    except (json.JSONDecodeError, ValueError):
        return None


def _format_events(events: list[dict] | None) -> str:
    """Format upcoming economic events for the Claude prompt."""
    if not events:
        return "  None upcoming"
    return "\n".join(
        "  - {title} ({ccy}, {imp} impact) in {mins} min{fc}".format(
            title=e["title"], ccy=e["currency"], imp=e["impact"],
            mins=e.get("minutes_until", "?"),
            fc=f" | Forecast: {e['forecast']}" if e.get("forecast") else "",
        )
        for e in events[:10]
    )


def _format_sentiment(sentiment: dict | None) -> str:
    """Format sentiment data for the Claude prompt."""
    if not sentiment:
        return "No sentiment data available"
    score = sentiment.get("score", 0)
    label = "bullish" if score > 0.2 else "bearish" if score < -0.2 else "neutral"
    reasoning = sentiment.get("reasoning", "")
    age = sentiment.get("age_minutes", "?")
    return f"{label} ({score:+.2f}), {age} min ago. {reasoning}"


def analyze_trade_signal(
    symbol: str,
    timeframe: str,
    strategy_name: str,
    signal_side: str,
    signal_reason: str,
    price: float,
    stop_loss: float | None,
    take_profit: float | None,
    candle_summary: list[dict],
    indicators: dict[str, Any],
    account_equity: float | None = None,
    open_positions: list[dict] | None = None,
    upcoming_events: list[dict] | None = None,
    sentiment: dict | None = None,
) -> dict:
    """Ask Claude to evaluate a trade signal. Returns analysis dict.

    On failure, returns a safe default that skips the trade.
    """
    client = _get_client()
    if client is None:
        return {"confidence": 0.0, "recommendation": "skip", "reasoning": "Claude API not available"}

    cfg = load_config()

    # Build concise candle summary (last 20 candles)
    recent = candle_summary[-20:] if len(candle_summary) > 20 else candle_summary
    candle_text = "\n".join(
        f"  {c.get('time', '?')}: O={c['open']:.5f} H={c['high']:.5f} "
        f"L={c['low']:.5f} C={c['close']:.5f}"
        for c in recent
    )

    indicator_text = "\n".join(f"  {k}: {v}" for k, v in indicators.items())

    position_text = "None"
    if open_positions:
        position_text = "\n".join(
            f"  {p['symbol']} {p['side']} @ {p.get('price', '?')} vol={p.get('volume', '?')}"
            for p in open_positions
        )

    user_prompt = (
        f"Evaluate this trade signal:\n\n"
        f"Symbol: {symbol}\n"
        f"Timeframe: {timeframe}\n"
        f"Strategy: {strategy_name}\n"
        f"Signal: {signal_side.upper()}\n"
        f"Price: {price:.5f}\n"
        f"Stop Loss: {f'{stop_loss:.5f}' if stop_loss else 'not set'}\n"
        f"Take Profit: {f'{take_profit:.5f}' if take_profit else 'not set'}\n"
        f"Reason: {signal_reason}\n\n"
        f"Recent candles ({timeframe}):\n{candle_text}\n\n"
        f"Current indicators:\n{indicator_text}\n\n"
        f"Account equity: {account_equity or 'unknown'}\n"
        f"Open positions:\n{position_text}\n\n"
        f"Upcoming economic events:\n{_format_events(upcoming_events)}\n\n"
        f"Market sentiment: {_format_sentiment(sentiment)}\n\n"
        f"Respond with a JSON object (keep reasoning short - we store and read it):\n"
        f'{{\n'
        f'  "confidence": 0.0-1.0,\n'
        f'  "recommendation": "execute" | "skip" | "wait",\n'
        f'  "adjusted_sl": <float or null>,\n'
        f'  "adjusted_tp": <float or null>,\n'
        f'  "reasoning": "<=200 chars: primary factor driving the decision"\n'
        f'}}'
    )

    try:
        response = client.messages.create(
            model=cfg.claude.model,  # signal generation → fast model (Sonnet)
            max_tokens=cfg.claude.max_tokens,
            temperature=cfg.claude.temperature,
            system=_build_system_prompt(),
            messages=[{"role": "user", "content": user_prompt}],
        )
        text = response.content[0].text
        parsed = _parse_json_response(text)
        if parsed is None:
            return {"confidence": 0.0, "recommendation": "skip", "reasoning": "Could not parse Claude response"}
        # Ensure required fields
        parsed.setdefault("confidence", 0.0)
        parsed.setdefault("recommendation", "skip")
        parsed.setdefault("reasoning", "")
        return parsed
    except Exception as exc:
        logger.error("Claude API error: %s", exc)
        return {"confidence": 0.0, "recommendation": "skip", "reasoning": f"API error: {exc}"}


