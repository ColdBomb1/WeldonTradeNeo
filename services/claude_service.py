def analyze_strategy_performance(scorecard: list[dict], signal_accuracy: dict) -> dict:
    """Ask Claude to review live trading performance and suggest adjustments.

    This is the Phase 3 feedback loop — Claude analyzes actual results
    and recommends strategy rotation or parameter changes.
    """
    client = _get_client()
    if client is None:
        return {"error": "Claude API not available"}

    cfg = load_config()

    if not scorecard:
        return {"error": "No performance data to analyze"}

    scorecard_text = "\n".join(
        "  {name}: {trades} trades, {wr:.1%} win rate, PF={pf:.2f}, "
        "P&L=${pnl:.2f}, MaxDD=${dd:.2f}".format(
            name=s["strategy"], trades=s["trades"], wr=s["win_rate"],
            pf=s["profit_factor"], pnl=s["total_pnl"], dd=s["max_drawdown"],
        )
        for s in scorecard
    )

    accuracy_text = "No confidence data"
    if signal_accuracy.get("total", 0) > 0:
        parts = []
        for level in ("high", "medium", "low"):
            bucket = signal_accuracy.get(f"{level}_confidence", {})
            if bucket.get("count"):
                acc = bucket.get("accuracy")
                parts.append(f"  {level}: {bucket['count']} signals, {acc:.1%} accuracy" if acc is not None else f"  {level}: {bucket['count']} signals")
        accuracy_text = "\n".join(parts) if parts else "No data"

    prompt = (
        "Review the live trading performance of our strategy portfolio:\n\n"
        f"Strategy scorecard:\n{scorecard_text}\n\n"
        f"Signal confidence accuracy:\n{accuracy_text}\n\n"
        "Analyze and respond with JSON:\n"
        '{\n'
        '  "summary": "overall assessment",\n'
        '  "degraded_strategies": ["strategies performing below expectations"],\n'
        '  "strong_strategies": ["strategies performing well"],\n'
        '  "recommendations": [\n'
        '    {"action": "disable|adjust|keep", "strategy": "name", "reason": "why"}\n'
        '  ],\n'
        '  "confidence_calibration": "is Claude confidence predicting outcomes well?"\n'
        '}'
    )

    try:
        response = _review_create(client, cfg, _build_system_prompt(), prompt)
        text = _extract_text(response)
        parsed = _parse_json_response(text)
        if parsed is None:
            return {"error": "Could not parse Claude response", "raw": text}
        return parsed
    except Exception as exc:
        logger.error("Claude API error: %s", exc)
        return {"error": str(exc)}


